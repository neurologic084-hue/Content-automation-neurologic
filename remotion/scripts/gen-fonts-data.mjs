// Generates src/fonts-data.ts — every public/fonts/*.ttf as a base64 data URI.
// The composition loads fonts from these embedded URIs instead of fetching them
// from Remotion's render server via staticFile(). Under a long render the
// per-page-mount font fetch intermittently hangs (delayRender "Loading font …"
// never clears → render fails); an embedded data URI has nothing to fetch, so
// it can't hang. Re-run after adding/removing a font:  node scripts/gen-fonts-data.mjs
import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const dir = path.dirname(fileURLToPath(import.meta.url))
const fontsDir = path.join(dir, '..', 'public', 'fonts')
const out = path.join(dir, '..', 'src', 'fonts-data.ts')

const files = readdirSync(fontsDir).filter((f) => f.endsWith('.ttf')).sort()
const entries = files.map((f) => {
  const b64 = readFileSync(path.join(fontsDir, f)).toString('base64')
  return `  ${JSON.stringify(f)}: "data:font/ttf;base64,${b64}",`
})

const banner = `// AUTO-GENERATED by scripts/gen-fonts-data.mjs — do not edit by hand.\n` +
  `// Fonts embedded as data URIs so the composition never fetches them from the\n` +
  `// render server (that fetch hangs on long sandbox renders). Re-run the script\n` +
  `// after changing public/fonts.\n`

writeFileSync(out, `${banner}export const FONT_DATA: Record<string, string> = {\n${entries.join('\n')}\n}\n`)
console.log(`wrote ${out} with ${files.length} fonts`)
